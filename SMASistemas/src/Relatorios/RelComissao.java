    public void relSintetico()
    {
        try
        {
            vmCampos = " COALESCE(sum(CR.CR_VALOR_DOCUMENTO),0) as TOTAL,  (COALESCE(sum(CR.CR_VALOR_DOCUMENTO),0) * FUN.FUNC_COMISSAO) / 100 as COMISSAO, ";
           
           vmCampos += " FUN.FUNC_NOME, FUN.FUNC_COMISSAO  "; 
           
           
           String vmParamentro = " INNER JOIN FUNCIONARIOS FUN ON (FUN.func_codigo_id = PED.PED_func_codigo_id) "+
                                 " INNER JOIN NOTA_FISCAL  NOTA ON (NOTA.NOT_CODIGO_ID = PED.ped_numero_nota_fiscal) "+
                                 " INNER JOIN CONTAS_RECEBER CR ON (CR.cr_nota_fiscal = PED.ped_numero_nota_fiscal) ";
                                 if(cbData.getSelectedIndex() == 0)
                                 {
                                     vmParamentro +=  " where CR.CR_data_EMISSAO >= '" + txtFaturamentoIni.getText() +"' AND CR.CR_data_EMISSAO <= '" + txtFaturamentoFim.getText() + "' ";
                                 }else
                                 {
                                     vmParamentro +=  " where CR.CR_data_vencimento >= '" + txtFaturamentoIni.getText() +"' AND CR.CR_data_vencimento <= '" + txtFaturamentoFim.getText() + "' ";
                                     
                                 }
                                  vmParamentro +=      " GROUP BY  FUN.FUNC_NOME,   FUN.FUNC_COMISSAO ";
                                 
           vmParamentro +=       " order by FUN.FUNC_nome ";
           
            
        rs = AcessoDados.Selecao("PEDIDOS PED", vmCampos, vmParamentro);
         }catch (Exception e) {
           JOptionPane.showMessageDialog(null,e.toString());
        }

    }
    public void RelAnalitico()
    {
        try
        {
            vmCampos = " COALESCE(sum(CR.CR_VALOR_DOCUMENTO),0) as TOTAL,  (COALESCE(sum(CR.CR_VALOR_DOCUMENTO),0) * FUN.FUNC_COMISSAO) / 100 as COMISSAO, ";
           
           vmCampos += " FUN.FUNC_NOME, FUN.FUNC_COMISSAO,  PAR.PAR_NOME_RAZAO_SOCIAL, PED.ped_data_faturamento, PED.ped_CODIGO_ID "; 
           
           
           String vmParamentro = " INNER JOIN FUNCIONARIOS FUN ON (FUN.func_codigo_id = PED.PED_func_codigo_id) "+
                                 " INNER JOIN NOTA_FISCAL  NOTA ON (NOTA.NOT_CODIGO_ID = PED.ped_numero_nota_fiscal) " +
                                 " INNER JOIN PARCEIROS PAR ON (PAR.PAR_CODIGO_ID = PED.PED_PAR_CODIGO_ID) "+
                                 " INNER JOIN CONTAS_RECEBER CR ON (CR.cr_nota_fiscal = PED.ped_numero_nota_fiscal) ";
                                 if(cbData.getSelectedIndex() == 0)
                                 {
                                     vmParamentro +=  " where CR.CR_data_EMISSAO >= '" + txtFaturamentoIni.getText() +"' AND CR.CR_data_EMISSAO <= '" + txtFaturamentoFim.getText() + "' ";
                                 }else
                                 {
                                     vmParamentro +=  " where CR.CR_data_vencimento >= '" + txtFaturamentoIni.getText() +"' AND CR.CR_data_vencimento <= '" + txtFaturamentoFim.getText() + "' ";
                                     
                                 }
                                 
                                 vmParamentro += " GROUP BY  FUN.FUNC_NOME,   FUN.FUNC_COMISSAO, PAR.PAR_NOME_RAZAO_SOCIAL, PED.ped_data_faturamento, PED.ped_CODIGO_ID ";
                                 
           vmParamentro +=       " order by FUN.FUNC_nome, PED.ped_data_faturamento desc";
           
            
        rs = AcessoDados.Selecao("PEDIDOS PED", vmCampos, vmParamentro);
         }catch (Exception e) {
           JOptionPane.showMessageDialog(null,e.toString());
        }

    }

