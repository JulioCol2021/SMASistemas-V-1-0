    private void txtProdutoIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtProdutoIDFocusLost
        // TODO add your handling code here:
        if (!txtProdutoID.getText().equals("")) {
            String vmCampos = "*";
            String vmCondicao_Consulta = " WHERE prod_codigo_produto = '" + txtProdutoID.getText() + "'";
            try {
                rs = AcessoDados.Selecao("produtos", vmCampos, vmCondicao_Consulta);
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Produto não cadastrado no sistema");
                    txtProdutoID.setText(null);
                    txtProduto.setText(null);
                    txtProdutoID.requestFocus();
                    return;
                }
                txtProduto.setText(rs.getString("prod_descricao"));
                rs.close();
            } catch (Exception e) {
                e.printStackTrace();
            }

        } else {
            txtProduto.setText(null);
        }

    }//GEN-LAST:event_txtProdutoIDFocusLost

