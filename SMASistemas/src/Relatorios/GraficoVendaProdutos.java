    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        if (txtEmpresa.getText().equals(null) || (txtEmpresa.getText().equals("")))
        {
           JOptionPane.showMessageDialog(null,"Favor informar a Empresa.");
           txtCodigoEmpresaID.requestFocus();
           return;
        }


          if (txtVencimentoIni.getText().equals("  /  /    ") || (txtVencimentoFim.getText().equals("  /  /    ")))
          {
             JOptionPane.showMessageDialog(null,"Período inválido.");
             txtVencimentoIni.requestFocus();
             return;
          }

       //Diferença de Dias entre datas
          
          
          
       vmCampos = " SELECT SUBSTRING(PROD_DESCRICAO,0,40) AS PRODUTO, " +
       " (SELECT coalesce(SUM(item_ped_quantidade),0) FROM ITEM_PEDIDO " +
       " INNER JOIN PEDIDOS ON (item_ped_pedido_codigo_id =  ped_codigo_id) " +
       " WHERE item_ped_prod_codigo_id = PROD_CODIGO_ID " +
       " AND PED_DATA_FATURAMENTO >= '"+txtVencimentoIni.getText()+"' AND PED_DATA_FATURAMENTO <= '"+txtVencimentoFim.getText()+"' ) AS QTDE_VENDA "
       + " FROM PRODUTOS " ;
       if(txtProdutoID.getText().length() > 0)
       {
          vmCampos += " WHERE PROD_CODIGO_PRODUTO = '"+txtProdutoID.getText() + "' ";
       }
       vmCampos += "  ORDER BY PROD_DESCRICAO LIMIT 10";


        try
        {
        rs = AcessoDados.SelecaoUNION(vmCampos);
         }catch (Exception e) {
           JOptionPane.showMessageDialog(null,e.getMessage());
        }

        HashMap map = new HashMap();

        try
        {
        map.put("Pempresa", txtEmpresa.getText());
        String caminho = getClass().getResource("/Imagens/Logo.jpg").toString();
        map.put("pLogo", caminho);
        map.put("pGrupo", "PERÍODO : " + txtVencimentoIni.getText() + " A " + txtVencimentoFim.getText());
        


        JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getClassLoader().getResource("Relatorios/GraProduto.jasper"));
        JRResultSetDataSource jrRS1 = new JRResultSetDataSource(rs);
        //JasperReport jr = (JasperReport) JRLoader.loadObject(arquivo);

        JasperPrint jp = JasperFillManager.fillReport(jr, map,jrRS1);
        JasperViewer jv = new JasperViewer(jp,false);
        JDialog viewer = new  JDialog(jv, true);
        viewer.setSize(1000,700);
        viewer.setLocationRelativeTo(null);
        viewer.getContentPane().add(jv.getContentPane());
        viewer.setVisible(true);

          }catch (Exception e) {
           JOptionPane.showMessageDialog(null,e.getMessage());
        }

        txtEmpresa.requestFocus();


    }//GEN-LAST:event_btnImprimirActionPerformed


