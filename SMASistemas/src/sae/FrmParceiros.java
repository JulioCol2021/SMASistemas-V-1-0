public void Comrpas()
{
    //MontaGrid();      
    String vmCampos = "*";
          String vmCondicao_Consulta = " WHERE NOT_PAR_CODIGO_ID = " + lbID.getText();
          try
            {
                rs = AcessoDados.Selecao("NOTA_FISCAL", vmCampos, vmCondicao_Consulta);
                javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)grCompras.getModel();
                double vTotal = 0;
                while (rs.next())
                {
                     dtm.addRow(new Object[]{rs.getString("not_ped_codigo_id"),
                                             rs.getString("not_codigo_id"),
                                             funcoes.formataData(rs.getString("not_emissao")),
                                             funcoes.formataMoeda(rs.getString("not_total"),"BD"),
                                             rs.getString("not_situacao_nfe")});
                                             
                     vTotal += rs.getDouble("not_total");
                }

                rs.close();
                txtTotal.setText(funcoes.formataMoeda(Double.toHexString(vTotal), "BD"));
                txtCompras.setText(Integer.toString(grCompras.getRowCount()));
            }catch (Exception e) {
           e.printStackTrace();
        }
    
}
