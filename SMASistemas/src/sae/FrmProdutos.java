    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtUnidadeMedida.getText().equals(null) || (txtUnidadeMedida.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Unidade de Medida não informada");
                tbPai.setSelectedIndex(0);
                txtUnidadeMedida.requestFocus();
                return;
            }

            if (txtCodigoProduto.getText().equals(null) || (txtCodigoProduto.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Código do produto não informado");
                tbPai.setSelectedIndex(0);
                txtCodigoProduto.requestFocus();
                return;
            }

            if (txtNCM.getText().equals(null) || (txtNCM.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Código do NCM não informado.");
                tbPai.setSelectedIndex(0);
                txtNCM.requestFocus();
                return;
            }

            if (txtDescricao.getText().equals(null) || (txtDescricao.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Descrição do produto não informada");
                tbPai.setSelectedIndex(0);
                txtDescricao.requestFocus();
                return;
            }

        if (txtDescricao.getText().equals(null) || (txtDescricao.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null,"Descrição não foi informada");
            txtDescricao.requestFocus();
            return;
        }
        funcoes.GravarDados(lbID.getText(), rootPane, "produtos", "prod_codigo_id",true);
        lbStatus.setText(" Registro");
        btnNovo.requestFocus();

    }//GEN-LAST:event_btnSalvarActionPerformed

