    public void MontaGridSeguranca()
    {
        int Linha = 0;
        String vmCampos = "*";
        String vmCondicao_Consulta = " where per_grupo ='SEGURANCA' and per_usu_codigo_id =" +txtCodigoID.getText();
        try
        {
        rs = AcessoDados.Selecao("permissao", vmCampos, vmCondicao_Consulta);
        while (rs.next())Linha++;
        rs.close();
        if(Linha > 0)
        {
            DefaultTableModel tableModel =(DefaultTableModel) grSeguranca.getModel();
            tableModel.setNumRows(0);
        }
        rs = AcessoDados.Selecao("permissao", vmCampos, vmCondicao_Consulta);
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)grSeguranca.getModel();
        while (rs.next())
        {
            dtm.addRow(new Object[]{rs.getString("per_tela"),rs.getBoolean("_001"),rs.getBoolean("_002"),rs.getBoolean("_003"),rs.getBoolean("_004"),rs.getBoolean("_005")});
        }
        rs.close();

         }catch (Exception e) {

         }
    }

