    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         if (txtDescricao.getText().equals(null) || (txtDescricao.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null,"Selecione um centro de custo antes de excluir.");
            return;
        }
        btnAlterar.setEnabled(false);
        btnNovo.setEnabled(false);
        btnExcluir.setEnabled(false);
        txtDescricao.requestFocus();
        variaveis.status = "E";
        lbStatus.setText("Excluindo... ");

        if(variaveis.status == "E")
        {
            Object[] options = { " Sim ", " Não " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Exclusão", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtDescricao.requestFocus();
                btnAlterar.setEnabled(true);
                btnNovo.setEnabled(true);
                btnExcluir.setEnabled(true);
                return;
               }
                 try
            {
               String vmCondicao_Exclusao ="CUSTO_CODIGO_ID = " + lbID.getText() ;

            AcessoDados.Excluir("Centro_custo", vmCondicao_Exclusao);

            }catch (Exception e) {
                e.printStackTrace();
            }
        }
        limpaCampos();
        btnCancelarActionPerformed(null);
        lbStatus.setText("Registro ");
        btnNovo.requestFocus();
        
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
         if (variaveis.status == "I")
        {
            if (txtDescricao.getText().equals(null) || (txtDescricao.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Centro de custo não foi informado");
                txtDescricao.requestFocus();
                return;
            }
        vmCampos1 = "*";
        vmCondicao_Consulta1 = " WHERE custo_centro_custo = '" + txtCentrodeCusto.getText() + "' ";
        try
        {
            rs = AcessoDados.Selecao("Centro_custo", vmCampos1, vmCondicao_Consulta1);
            if (rs.next())
        {
            JOptionPane.showMessageDialog(null,"Centro de custo já cadastrado no sistema!");
            btnCancelarActionPerformed(null);
            return;
        }
        rs.close();
        }catch (Exception e) {
          e.printStackTrace();
        }
            Object[] options = { " Sim ", " Não " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Inclusão", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtDescricao.requestFocus();
                return;
               }
            try
            {
               String vmCampos = "CUSTO_CENTRO_CUSTO,"+"CUSTO_DESCRICAO";

               String vmParametros = "'" + txtCentrodeCusto.getText() + "'," +
                                     "'" + txtDescricao.getText()+ "'";
                                          

            AcessoDados.Inserir("Centro_custo", vmCampos, vmParametros);

            }catch (Exception e) {
                e.printStackTrace();
            }
        }
        if(variaveis.status == "A")
        {
        vmCampos1 = "*";
        vmCondicao_Consulta1 = " WHERE custo_centro_custo = '" + txtCentrodeCusto.getText() + "' " +
                                "and custo_descricao ='" + txtDescricao.getText() + "'";
        try
        {
            rs = AcessoDados.Selecao("Centro_custo", vmCampos1, vmCondicao_Consulta1);
            if (rs.next())
        {
            JOptionPane.showMessageDialog(null,"Centro de custo já cadastrado no sistema!");
            //btnCancelarActionPerformed(null);
            return;
        }
        rs.close();
        }catch (Exception e) {
          e.printStackTrace();
        }
            Object[] options = { " Sim ", " Não " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Alteração", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtDescricao.requestFocus();
                return;
               }
                 try
            {
               String vmCampos_Parametros = "CUSTO_CENTRO_CUSTO = '" + txtCentrodeCusto.getText() + "'," +
                                            "CUSTO_DESCRICAO = '" + txtDescricao.getText() + "'" ;


               String vmCondicao_Alteracao ="CUSTO_CODIGO_ID = " + lbID.getText() ;

            AcessoDados.Alterar("Centro_custo", vmCampos_Parametros, vmCondicao_Alteracao);

            }catch (Exception e) {
                e.printStackTrace();
            }
        }
        limpaCampos();
        btnCancelarActionPerformed(null);
        lbStatus.setText("Registro ");
        btnNovo.requestFocus();
       
    }//GEN-LAST:event_btnSalvarActionPerformed

