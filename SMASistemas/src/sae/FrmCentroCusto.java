    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        // TODO add your handling code here:
        FpesqPesquisa md = new FpesqPesquisa(null, true);
        md.setLocationRelativeTo(null);
        md.setVisible(true);

       if(!md.getRetorno().trim().equals("") ||!md.getRetorno().equals(null))
        {
            lbID.setText((String) md.getRetorno());
        }
        if (!lbID.getText().equals("")) {
            String vmCampos = "*";
            String vmCondicao_Consulta = " WHERE custo_codigo_id = " + lbID.getText();
            try {
                rs = AcessoDados.Selecao("centro_custo", vmCampos, vmCondicao_Consulta);
                rs.next();
                txtCentrodeCusto.setText(rs.getString("custo_centro_custo"));
                txtDescricao.setText(rs.getString("custo_descricao"));

                rs.close();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

