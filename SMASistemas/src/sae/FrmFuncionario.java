    private void txtDepartamentoCodigoIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtDepartamentoCodigoIDFocusLost
        // TODO add your handling code here:
       if(!txtDepartamentoCodigoID.getText().equals(""))
       {
          String vmCampos = "*";
          String vmCondicao_Consulta = " WHERE depa_codigo_id = " + txtDepartamentoCodigoID.getText();
          try
        {
        rs = AcessoDados.Selecao("Departamento", vmCampos, vmCondicao_Consulta);
        if (!rs.next())
        {
            JOptionPane.showMessageDialog(null,"Departamento n√£o cadastrado no sistema");
            txtDepartamentoCodigoID.setText(null);
            txtDepartamento.setText(null);
            txtDepartamentoCodigoID.requestFocus();
            return;
        }
        txtDepartamento.setText(rs.getString("depa_descricao"));
        txtCargo.requestFocus();
        rs.close();
          }catch (Exception e) {
           e.printStackTrace();
        }

       }
       else
           txtDepartamento.setText("");
    }//GEN-LAST:event_txtDepartamentoCodigoIDFocusLost

