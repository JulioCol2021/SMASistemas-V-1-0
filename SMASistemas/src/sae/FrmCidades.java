    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
       if (txtMunicipio.getText().equals(null) || (txtMunicipio.getText().equals("")))
       {
           JOptionPane.showMessageDialog(null,"Cidade não foi informada");
           txtCodigoUF.requestFocus();
           return;
       }
        String vmCampos1 = "*";
        String vmCondicao_Consulta1 = " WHERE cid_codigo_uf = '" + txtCodigoUF.getText() + "'" +
                                      " and cid_uf = '" + ddlUF.getSelectedItem().toString() + "'" +
                                      " and cid_codigo_municipio = '" + txtCodigoMunicipio.getText() + "'" +
                                      " and cid_municipio = '" + txtMunicipio.getText() + "'";
        try
        {
           rs = AcessoDados.Selecao("Cidades", vmCampos1, vmCondicao_Consulta1);
           if (rs.next())
           {
             JOptionPane.showMessageDialog(null,"Cidade já cadastrada no sistema!");
             return;
           }
        }catch (Exception e) {
           e.printStackTrace();
        }
        funcoes.GravarDados(lbID.getText(), rootPane, "cidades", "cid_codigo_id",true);
        lbStatus.setText(" Registro");
        btnNovo.requestFocus();
      
    }//GEN-LAST:event_btnSalvarActionPerformed

