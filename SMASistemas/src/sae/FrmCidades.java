    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        if(funcoes.Permissao("005", variaveis.usuario_id, variaveis.usuario_super, this.getTitle()))
        {
        btnImprimir.setEnabled(false);
        setCursor(Cursor.getPredefinedCursor(Cursor.WAIT_CURSOR));
        String vmCampos = "*";
        String vmCondicao_Consulta = "order by cid_municipio";
        try
        {
        rs = AcessoDados.Selecao("cidades", vmCampos, vmCondicao_Consulta);
         }catch (Exception e) {
           e.printStackTrace();
        }

        HashMap map = new HashMap();

        try
        {
          JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getClassLoader().getResource("Relatorios/RelCidades.jasper"));
          JRResultSetDataSource jrRS1 = new JRResultSetDataSource(rs);
          JasperPrint jp = JasperFillManager.fillReport(jr, map,jrRS1);
          JasperViewer jv = new JasperViewer(jp,false);
          JDialog viewer = new  JDialog(jv, true);
          viewer.setSize(1000,700);
          viewer.setLocationRelativeTo(null);
          viewer.getContentPane().add(jv.getContentPane());
          viewer.setVisible(true);

        }catch (Exception e) {
           e.printStackTrace();
        }
        btnImprimir.setEnabled(true);
        setCursor(Cursor.getPredefinedCursor(Cursor.DEFAULT_CURSOR));

        }
    }//GEN-LAST:event_btnImprimirActionPerformed

