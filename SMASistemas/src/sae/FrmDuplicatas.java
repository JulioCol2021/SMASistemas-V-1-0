    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        // TODO add your handling code here:
        if (txtEmpresa.getText().equals(null) || (txtEmpresa.getText().equals("")))
        {
           JOptionPane.showMessageDialog(null,"Favor informar a Empresa.");
           txtCodigoEmpresaID.requestFocus();
           return;
        }
        if (txtVencimentoIni.getText().equals("  /  /    ") || (txtVencimentoFim.getText().equals("  /  /    ")))
        {
           JOptionPane.showMessageDialog(null,"Favor informar per챠odo de emiss찾o.");
           txtVencimentoIni.requestFocus();
           return;
        }
        if (cbMatricial.isSelected())
        {
           JOptionPane.showMessageDialog(null,"Modelo n찾o Disponiv챕l." + "\n" + "Solicite ao suporte a configura챌찾o pr챕-impressa!");
           txtEmpresa.requestFocus();
           return;
        }
        

        String vmCampos = "";
        vmCampos += " CR.*, fu_extenso_real(cast(CR.cr_valor_documento as numeric)) as extenso, "
                 +  " PAR.*, EMP.*, CID_EMP.CID_MUNICIPIO AS CID_EMP, CID_EMP.CID_UF AS UF_EMP, "
                 +  " CID_PAR.CID_MUNICIPIO AS CID_PAR, CID_PAR.CID_UF AS UF_PAR  ";
        
       
        String vmCondicao_Consulta = "";
        vmCondicao_Consulta += " INNER JOIN PARCEIROS   PAR ON (PAR_CODIGO_ID = CR.CR_PAR_CODIGO_ID)";
        vmCondicao_Consulta += " INNER JOIN EMPRESAS    EMP ON (EMP.EMP_CODIGO_ID = CR.CR_EMP_CODIGO_ID)";
        vmCondicao_Consulta += " LEFT  JOIN CIDADES CID_PAR ON (CID_PAR.CID_CODIGO_ID = PAR.par_cidade_comercial_codigo_id)";
        vmCondicao_Consulta += " LEFT  JOIN CIDADES CID_EMP ON (CID_EMP.CID_CODIGO_ID = EMP.EMP_cid_codigo_id)";

        
        vmCondicao_Consulta  += " WHERE CR_EMP_CODIGO_ID = " + txtCodigoEmpresaID.getText();
        
        vmCondicao_Consulta += " AND (CR_DATA_EMISSAO >= '"+txtVencimentoIni.getText()+"' AND CR_DATA_EMISSAO <= '"+txtVencimentoFim.getText()+"') ";

        if(!txtCodigoParceiroID.getText().equals(""))
        {
           vmCondicao_Consulta += " AND CR_PAR_CODIGO_ID = " + txtCodigoParceiroID.getText();
        }

        if(!txtBancoID.getText().equals(""))
        {
           vmCondicao_Consulta += " AND cr_banc_codigo_id = " + txtBancoID.getText();
        }

        vmCondicao_Consulta += " ORDER BY CR_DATA_EMISSAO DESC";
        try
        {
        rs = AcessoDados.Selecao("CONTAS_RECEBER CR", vmCampos, vmCondicao_Consulta);
         }catch (Exception e) {
           e.printStackTrace();
        }

        HashMap map = new HashMap();  

        try
        {
        String caminho = getClass().getResource("/Imagens/Logo.jpg").toString();
        map.put("pLogo", caminho);

        JasperReport jr = (JasperReport) JRLoader.loadObject(getClass().getClassLoader().getResource("Relatorios/Duplicata.jasper"));
        JRResultSetDataSource jrRS1 = new JRResultSetDataSource(rs);
        //JasperReport jr = (JasperReport) JRLoader.loadObject(arquivo);

        JasperPrint jp = JasperFillManager.fillReport(jr, map,jrRS1);
        JasperViewer jv = new JasperViewer(jp,false);
        JDialog viewer = new  JDialog(jv, true);
        viewer.setSize(1000,700);
        viewer.setLocationRelativeTo(null);
        viewer.getContentPane().add(jv.getContentPane());
        viewer.setVisible(true);

          }catch (Exception e) {
           e.printStackTrace();
        }

        txtEmpresa.requestFocus();


    }//GEN-LAST:event_btnImprimirActionPerformed

