    public void BuscarRecebimento()
    {
        DefaultTableModel tableModel =(DefaultTableModel) grRecebimento.getModel();
        tableModel.setNumRows(0);

        String vmCampos = " rcr_codigo_id, rcr_valor_recebido,  to_char(rcr_data_recebimento, 'DD/MM/YYYY') AS rcr_data_recebimento, rcr_usuario, rcr_acrescimo, rcr_desconto";
        ButtonColumn buttonColumn = new ButtonColumn(grRecebimento, 6);
        String vmCondicao_Consulta = "";
        vmCondicao_Consulta = " where rcr_cr_codigo_id = " + lbID.getText() + " order by rcr_data_recebimento";
        vRecebido =0;
        try
        {
        rs = AcessoDados.Selecao("recebimento_contas_receber  ", vmCampos, vmCondicao_Consulta);
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)grRecebimento.getModel();
        while (rs.next())
        {
            dtm.addRow(new Object[]{rs.getString("rcr_codigo_id"),
                                    funcoes.formataMoeda(rs.getString("rcr_valor_recebido"),"BD"),
                                    rs.getString("rcr_data_recebimento"),
                                    funcoes.formataMoeda(rs.getString("rcr_acrescimo"),"BD"),
                                    funcoes.formataMoeda(rs.getString("rcr_desconto"),"BD"),
                                    rs.getString("rcr_usuario"),
                                    ""});
            vRecebido += rs.getDouble("rcr_valor_recebido");
        }
        rs.close();
        double vTitulo = Double.parseDouble(txtValorDocumento.getText().replace(".", "").replace(",", "."));
        double vPendente = vTitulo - vRecebido;
        lbPendente.setText(funcoes.formataMoeda(Double.toString(vPendente),"BD"));

        }catch (Exception e) {
           e.printStackTrace();
        }



    }
