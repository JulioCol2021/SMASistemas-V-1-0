    private void registraEnterNoBotao(JButton b) {
        b.registerKeyboardAction(
                b.getActionForKeyStroke(
                KeyStroke.getKeyStroke(KeyEvent.VK_SPACE, 0, false)),
                KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0, false),
                JComponent.WHEN_FOCUSED);

        b.registerKeyboardAction(
                b.getActionForKeyStroke(
                KeyStroke.getKeyStroke(KeyEvent.VK_SPACE, 0, true)),
                KeyStroke.getKeyStroke(KeyEvent.VK_ENTER, 0, true),
                JComponent.WHEN_FOCUSED);
    }
    public void RetornaDadosGrid()
    {
        lbID.setText(grDados.getValueAt(grDados.getSelectedRow(), 0).toString());
        txtNome.setText(grDados.getValueAt(grDados.getSelectedRow(), 1).toString());
        ddlAtivo.setSelectedItem(grDados.getValueAt(grDados.getSelectedRow(), 2).toString());
        ddlSuper.setSelectedItem(grDados.getValueAt(grDados.getSelectedRow(), 3).toString());
       
    }
    public void MontaGrid()
    {
        String vmCampos = "*";
        String vmCondicao_Consulta = " order by usu_codigo_id";
        try
        {
        rs = AcessoDados.Selecao("Usuarios", vmCampos, vmCondicao_Consulta);
        grDados.setModel(new javax.swing.table.DefaultTableModel(
                      new Object [][] { },
                      new String [] {"Código ","Nome", "Ativo","Super" }));
        javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)grDados.getModel();
        while (rs.next())
        {
            dtm.addRow(new Object[]{rs.getString("USU_CODIGO_ID"),rs.getString("USU_NOME"),rs.getString("USU_ATIVO"),rs.getString("USU_SUPER")});
        }
        rs.close();
        grDados.getColumnModel().getColumn(0).setPreferredWidth(15);
        grDados.getColumnModel().getColumn(1).setPreferredWidth(400);
        grDados.getColumnModel().getColumn(2).setPreferredWidth(15);
        grDados.getColumnModel().getColumn(3).setPreferredWidth(15);

        DefaultTableCellRenderer esquerda = new DefaultTableCellRenderer();
        DefaultTableCellRenderer centralizado = new DefaultTableCellRenderer();
        DefaultTableCellRenderer direita = new DefaultTableCellRenderer();

        esquerda.setHorizontalAlignment(SwingConstants.LEFT);
        centralizado.setHorizontalAlignment(SwingConstants.CENTER);
        direita.setHorizontalAlignment(SwingConstants.RIGHT);

        grDados.getColumnModel().getColumn(2).setCellRenderer(centralizado);
        grDados.getColumnModel().getColumn(3).setCellRenderer(centralizado);
    
         }catch (Exception e) {
           e.printStackTrace();
        }
    }

 
