    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (variaveis.status == "I")
        {
            if (txtNome.getText().equals(null) || (txtNome.getText().equals("")))
            {
                JOptionPane.showMessageDialog(null,"Nome do usuário não foi informado");
                txtNome.requestFocus();
                return;
            }
            if ((txtSenha.getText().compareTo(txtConfirma.getText()) != 0))
            {
               JOptionPane.showMessageDialog(null,"Senha confirmada não confere");
               txtSenha.setText(null);
               txtConfirma.setText(null);
               txtSenha.requestFocus();
               return;
            }
            Object[] options = { " Sim ", " Não " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Inclusão", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtNome.requestFocus();
                return;
               }
            try
            {
               String vmCampos = "USU_NOME,"  +
                                 "USU_SENHA," +
                                 "USU_ATIVO," +
                                 "USU_SUPER";

               String vmParametros = "'" + txtNome.getText() + "'," +
                                     "'" + txtSenha.getText() + "'," +
                                     "'" + ddlAtivo.getSelectedItem().toString() + "'," +
                                     "'" + ddlSuper.getSelectedItem().toString() + "'";

            AcessoDados.Inserir("Usuarios", vmCampos, vmParametros);

            }catch (Exception e) {
                e.printStackTrace();
            }
        }
        if(variaveis.status == "A")
        {
            Object[] options = { " Sim ", " Não " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Alteração", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtNome.requestFocus();
                return;
               }
                 try
            {
               String vmCampos_Parametros = "USU_ATIVO = '" + ddlAtivo.getSelectedItem().toString() + "', " +
                                            "USU_SUPER = '" + ddlSuper.getSelectedItem().toString() +"'";

               String vmCondicao_Alteracao ="USU_CODIGO_ID = " + lbID.getText() ;

            AcessoDados.Alterar("Usuarios", vmCampos_Parametros, vmCondicao_Alteracao);

            }catch (Exception e) {
                e.printStackTrace();
            }
        }
        limpaCampos();
        btnCancelarActionPerformed(null);
        lbStatus.setText("Alterando... ");
        btnNovo.requestFocus();
        MontaGrid();
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if (txtNome.getText().equals(null) || (txtNome.getText().equals("")))
        {
            JOptionPane.showMessageDialog(null,"Selecione um usuário antes de alterar.");
            return;
        }
        btnCancelar.setEnabled(true);
        btnSalvar.setEnabled(true);
        txtNome.setEnabled(false);
        txtSenha.setEnabled(false);
        txtConfirma.setEnabled(false);
        ddlAtivo.setEnabled(true);
        ddlSuper.setEnabled(true);
        btnAlterar.setEnabled(false);
        btnNovo.setEnabled(false);
        grDados.setEnabled(false);
        txtNome.requestFocus();
        variaveis.status = "A";
        lbStatus.setText("Alterando... ");
    }//GEN-LAST:event_btnAlterarActionPerformed

