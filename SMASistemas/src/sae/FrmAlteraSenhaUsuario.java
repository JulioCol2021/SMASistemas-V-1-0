    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        if (txtSenhaAtual.getText().equals(""))
        {
            JOptionPane.showMessageDialog(null,"Senha atual n√£o informada");
            txtSenhaAtual.requestFocus();
             return;
        }
        if ((txtSenha.getText().compareTo(txtConfirma.getText()) != 0))
        {
           JOptionPane.showMessageDialog(null,"Senha confirmada n√£o confere");
           txtSenha.setText(null);
           txtConfirma.setText(null);
           txtSenha.requestFocus();
           return;
        }
        if (!txtSenhaAtual.getText().equals("")) {
            String vmCampos = "*";
            String vmCondicao_Consulta = " WHERE usu_codigo_id = " + lbID.getText() + " and usu_senha ='"+ txtSenhaAtual.getText() + "'";
            try {
                rs = AcessoDados.Selecao("usuarios", vmCampos, vmCondicao_Consulta);
                if (!rs.next()) {
                    JOptionPane.showMessageDialog(null, "Senha n√£o confere com a atual cadastrada!");
                    txtSenhaAtual.requestFocus();
                    return;
                }
                txtSenhaAtual.setText(rs.getString("usu_senha"));
                rs.close();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        Object[] options = { " Sim ", " N√£o " };
            int confirmar = JOptionPane.showOptionDialog(null, "Confirma Altera√ß√£o", " Mensagem...",JOptionPane.DEFAULT_OPTION, JOptionPane. QUESTION_MESSAGE,null,options, options[0]);
            if(confirmar==1){
                txtNome.requestFocus();
                return;
            }
            try
            {
               String vmCampos_Parametros = "usu_senha = '" + txtConfirma.getText() + "'";

               String vmCondicao_Alteracao ="usu_codigo_id = "+ lbID.getText();

            AcessoDados.Alterar("Usuarios", vmCampos_Parametros, vmCondicao_Alteracao);

            }catch (Exception e) {
                e.printStackTrace();
            }
   
        JOptionPane.showMessageDialog(null,"Senha alterada com sucesso");
        btnCancelarActionPerformed(null);
        lbStatus.setText("Registro ");
        
    }//GEN-LAST:event_btnSalvarActionPerformed

